package ch03.sec01;

public class AssignmentOperatorExample {
	public static void main(String args[]) {
		int result = 0;
		
		result += 10;
		System.out.println(result);
		result -= 5;
		System.out.println(result);
		result /= 2;
		System.out.println(result);
		
		char a = 'a';
		System.out.println(a);
		
	}

}
